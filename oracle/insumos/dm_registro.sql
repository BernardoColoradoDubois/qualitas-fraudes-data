BEGIN
   EXECUTE IMMEDIATE 'DROP TABLE INSUMOS.DM_REGISTRO CASCADE CONSTRAINTS';
EXCEPTION
   WHEN OTHERS THEN
      IF SQLCODE != -942 THEN
         RAISE;
      END IF;
END;


